enum custom_keycodes {
    K_TILDE = SAFE_RANGE,
    K_GRAVE,
    K_CARET,
    K_UNDS,
    K_LAYER1,
};

enum {
    TD_SHIFT_CAPS = 0,
    TD_CTRL_ESC,
    TD_ENTER_TAB,
};

void shift_dance_finished_fn(tap_dance_state_t* state, void* _) {
    if (state->count >= 2) {
        tap_code(KC_CAPS);
    } else if (state->pressed) {
        register_code(KC_LSFT);
    } else {
        add_oneshot_mods(MOD_BIT(KC_LSFT));
    }
}

void shift_dance_reset_fn(tap_dance_state_t* state, void* _) {
    unregister_code(KC_LSFT);
}

void ctrl_dance_finished_fn(tap_dance_state_t* state, void* _) {
    if (state->count >= 2) {
        tap_code(KC_ESC);
    } else if (state->pressed) {
        register_code(KC_LCTL);
    } else {
        add_oneshot_mods(MOD_BIT(KC_LCTL));
    }
}

void ctrl_dance_reset_fn(tap_dance_state_t* state, void* _) {
    unregister_code(KC_LCTL);
}

// Tap dances
tap_dance_action_t tap_dance_actions[] = {
    [TD_SHIFT_CAPS] = ACTION_TAP_DANCE_FN_ADVANCED_WITH_RELEASE(NULL, NULL, shift_dance_finished_fn, shift_dance_reset_fn),
    [TD_CTRL_ESC] = ACTION_TAP_DANCE_FN_ADVANCED_WITH_RELEASE(NULL, NULL, ctrl_dance_finished_fn, ctrl_dance_reset_fn),
    [TD_ENTER_TAB] = ACTION_TAP_DANCE_DOUBLE(KC_ENTER, KC_TAB),
};

void altgr_modifier(uint16_t keycode) {
    register_code(KC_RALT);
    tap_code16(keycode);
    unregister_code(KC_RALT);
}

// Custom keycode handler
bool process_record_user(uint16_t keycode, keyrecord_t* record) {
    // Switch to layer when tapped, momentarily switch to layer when held
    static uint16_t k_layer1_timer;
    if (keycode == K_LAYER1) {
        if (record->event.pressed) {
            k_layer1_timer = timer_read(); // Start the timer when key is pressed
            layer_on(1);
        } else {
            layer_clear();

            if (timer_elapsed(k_layer1_timer) < LAYER_SWITCH_TAPPING_TERM) {
                layer_move(1);
            }
        }
        return false;
    } else {
        k_layer1_timer -= LAYER_SWITCH_TAPPING_TERM;
    }

    if (!record->event.pressed)
        return true;

    switch (keycode) {
        case K_TILDE: // "~" symbol
            altgr_modifier(KC_N);
            tap_code(KC_SPC);
            return false;
        case K_GRAVE: // "`" symbol
            register_code(KC_LSFT);
            tap_code(DE_ACUT);
            unregister_code(KC_LSFT);
            tap_code(KC_SPC);
            return false;
        case K_CARET: // "^" symbol
            tap_code(DE_CIRC);
            tap_code(KC_SPC);
            return false;
        case K_UNDS: // "_" symbol
            // register_code(KC_RSFT);
            tap_code16(DE_QUOT);
            // unregister_code(KC_RSFT);
            return false;
    }
    return true;
}

